export const Route = createRootRoute({
  head: () => ({
    meta: [
      { charSet: "utf-8" },
      { name: "viewport", content: "width=device-width, initial-scale=1, viewport-fit=cover" },
      { title: "SpendWise — Daily Expense Tracker" },
      { name: "description", content: "Track daily expenses, set budgets, and gain insights into your spending habits." },
      { property: "og:title", content: "SpendWise — Daily Expense Tracker" },
      { property: "og:description", content: "Track daily expenses, set budgets, and gain insights into your spending habits." },
      { property: "og:type", content: "website" },
      { name: "twitter:card", content: "summary" },
    ],
    links: [
      { rel: "stylesheet", href: appCss },
    ],
  }),
  shellComponent: RootShell,
  component: RootComponent,
  notFoundComponent: NotFoundComponent,
});

function RootShell({ children }: { children: React.ReactNode }) {
  return (
    <html lang="en" suppressHydrationWarning>
      <head>
        <HeadContent />
        <script dangerouslySetInnerHTML={{
          __html: `try{var t=localStorage.getItem("et_theme");var d=t==="dark"||(t!=="light"&&matchMedia("(prefers-color-scheme:dark)").matches);if(d)document.documentElement.classList.add("dark")}catch(e){}`
        }} />
      </head>
      <body>
        {children}
        <Scripts />
      </body>
    </html>
  );
}

function RootComponent() {
  return (
    <ThemeProvider>
      <div className="min-h-screen pb-20">
        <Outlet />
      </div>
      <BottomNav />
      <Toaster position="top-center" richColors />
    </ThemeProvider>
  );
}
